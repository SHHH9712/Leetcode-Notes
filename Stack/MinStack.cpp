class MinStack {
public:
    stack<int> container;
    stack<int> min;
    MinStack() {
        container = {};
        min = {};
    }
    
    void push(int x) {
        if(container.size()==0 || x<=min.top() ){
            min.push(x);
        }
        container.push(x);
    }
    
    void pop() {
        if(container.top()==min.top()){
            min.pop();
        }
        container.pop();
    }
    
    int top() {
        return container.top();
    }
    
    int getMin() {
        return min.top();
    }
};

